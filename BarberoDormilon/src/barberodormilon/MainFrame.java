    public void setEstadoBarbero(){
        if(barbero.isDurmiendo()){
            lblBarbero.setText("Barbero durmiendo");
        }else{
            lblBarbero.setText("Barbero trabajando");
        }
    }
    
    public void setEstadoSilla(){
        if(silla.isServidor()){
            lblSilla.setText("Estado: Trabajando");
        }else{
            lblSilla.setText("Estado: ocio");
        }
    }
    
    public void setContadorEspera(){
        lblEspera.setText("Sala de espera: "+silla.getColaEspera());
    }

